    private void initListaModeloImpressora(){
        DataResponseModel<List<ModeloImpressora>> resp = this.modeloImpressoraController.findAll();
        if(!resp.isSuccess()){
            FrMain.exibirPopUp(resp.getMessage());
            return;
        }
        this.modelos = resp.getData();
    }

    private void renderModeloImpressoraList() {
        this.contentPanel = new JPanel();
        this.contentPanel.setLayout(new GridLayout(0, 4));
        this.jpBody.setLayout(new BoxLayout(jpBody, BoxLayout.Y_AXIS));

        if (this.modelos == null || this.modelos.isEmpty()) {
            return;
        }

        for (ModeloImpressora imp : this.modelos) {
            this.contentPanel.add(new ModeloImpressoraCard(imp));
        }

        this.jspListaImpressora = new JScrollPane(this.contentPanel);
        this.jspListaImpressora.setVerticalScrollBarPolicy(JScrollPane.VERTICAL_SCROLLBAR_ALWAYS);
        this.jpBody.add(this.jspListaImpressora);
        this.revalidate();
        this.repaint();
    }
